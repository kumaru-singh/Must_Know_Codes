// A lambda function in C++ is an anonymous function that can be defined inline without explicitly naming it. It was introduced in C++11 and is useful for short, one-time-use functions.

// Basic Syntax of a Lambda Function

// `cpp`
// [capture](parameters) -> return_type { function_body };

// Capture: Specifies which variables from the surrounding scope the lambda can access.
// Parameters: List of parameters (like a normal function).
// Return Type (optional): The type of value returned.
// Function Body: The actual implementation.